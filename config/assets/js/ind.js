$(document).ready(function() {
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    //tag
    $('#mobile-opener').click(function() {
        $('div#links_class').toggleClass('showed');
    });
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    $('#share').click(function() {
        $('.share_pop').addClass('share_popup');
    });
    $('.share#remove').click(function() {
        $('.share_pop').removeClass('share_popup');
    });
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    $('#remove').click(function() {
        $('#notice').hide();
    });
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    //--------------------------------------------------------------------------------
    const stickyTop = function() {
        let scrollTop = $(window).scrollTop();
        if (scrollTop > 100) {
            $('.to-top').addClass('to-topview');
        } else {
            $('.to-top').removeClass('to-topview');
        }
    };
    stickyTop();
    // and run it again every time you scroll
    $(window).scroll(function() {
        stickyTop();
    });
    $('.to-top').on("click", function() {
        $('html, body').animate({ scrollTop: 0 }, 'slow');
    });
});
